efi_status gop_init(efi_handle image_handle, efi_system_table *st) {
    (void)image_handle;
    efi_boot_services *bs = st->BootServices;
    efi_guid gop_guid = EFI_GOP_GUID;
    efi_status status;
    efi_gop_mode_info *info = NULL;
    u64 info_size = 0;

    status = ((efi_bs_locate_protocol)bs->LocateProtocol)(&gop_guid, NULL, (void**)&gop);
    if (EFI_ERROR(status)) return status;
    if (!gop || !gop->Mode) return EFI_ERR(1);

    status = ((efi_gop_set_mode)gop->SetMode)(gop, gop->Mode->Mode);
    if (EFI_ERROR(status)) return status;

    status = gop->QueryMode(gop, gop->Mode->Mode, &info_size, &info);
    if (EFI_ERROR(status) || !info) {
        info = gop->Mode->Info;
    }

    fb.base  = gop->Mode->FrameBufferBase;
    fb.size  = gop->Mode->FrameBufferSize;
    fb.width = info->HorizontalResolution;
    fb.height = info->VerticalResolution;
    fb.pitch = info->PixelsPerScanLine * 4;
    fb.bpp = 32;
    fb.pixel_format = info->PixelFormat;

    return EFI_SUCCESS;
}

void gop_put_pixel(u32 x, u32 y, u32 color) {
    if (x >= fb.width || y >= fb.height) return;
    u32 *ptr = (u32*)(usize)fb.base;
    ptr[y * (fb.pitch / 4) + x] = color;
}

u32 gop_get_pixel(u32 x, u32 y) {
    if (x >= fb.width || y >= fb.height) return 0;
    u32 *ptr = (u32*)(usize)fb.base;
    return ptr[y * (fb.pitch / 4) + x];
}

void gop_fill_rect(u32 x, u32 y, u32 w, u32 h, u32 color) {
    if (x >= fb.width || y >= fb.height) return;
    if (x + w > fb.width) w = fb.width - x;
    if (y + h > fb.height) h = fb.height - y;
    u32 *base = (u32*)(usize)fb.base;
    u32 pitch_px = fb.pitch / 4;
    for (u32 j = 0; j < h; j++) {
        u32 *line = base + (y + j) * pitch_px + x;
        for (u32 i = 0; i < w; i++)
            line[i] = color;
    }
}

void gop_draw_char(u32 x, u32 y, u32 fg, u32 bg, char c) {
    if (c < 32 || c > 126) c = '.';
    if (x + 8 > fb.width || y + 16 > fb.height) return;
    u32 *base = (u32*)(usize)fb.base;
    u32 pitch_px = fb.pitch / 4;
    int idx = c - 32;
    for (int row = 0; row < 16; row++) {
        u8 bits = font_8x16[idx][row];
        u32 *line = base + (y + row) * pitch_px + x;
        if (bits == 0xFF) {
            line[0] = fg; line[1] = fg; line[2] = fg; line[3] = fg;
            line[4] = fg; line[5] = fg; line[6] = fg; line[7] = fg;
        } else if (bits == 0x00) {
            line[0] = bg; line[1] = bg; line[2] = bg; line[3] = bg;
            line[4] = bg; line[5] = bg; line[6] = bg; line[7] = bg;
        } else {
            for (int col = 0; col < 8; col++)
                line[col] = (bits & (0x80 >> col)) ? fg : bg;
        }
    }
}

void gop_draw_string(u32 x, u32 y, u32 fg, u32 bg, const char *str) {
    while (*str) {
        gop_draw_char(x, y, fg, bg, *str);
        x += 8;
        str++;
    }
}

u32 gop_make_color(u8 r, u8 g, u8 b) {
    if (fb.pixel_format == 0) {
        return r | (g << 8) | (b << 16);
    }
    return (r << 16) | (g << 8) | b;
}

